# O basico de operadores
print(5 + 5)
print(5 - 5)
print(5 * 5)

# divisao normal
print(9 / 5)

# divisão só com números inteiros
print(9 // 5)

# potenciação
print(2 ** 3)

# atribuindo variaveis
counter = 11
print(counter)

# Operações com variáveis
counter = 12
counter -= 5
print(counter)

# Operações de comparação
print(10 < 2)
print(10 > 2)

